package p01ArithmeticOperators;

public class Average {
    public static void main(String[] args) {

        double num1 = 6;
        double num2 = 4;
        double num3 = 3;
        double average;

        average = (num1 + num2 + num3 )/3;
        System.out.println("The average of " + num1 + ", " + num2 + " and " + num3 + " is : " + average);
    }
}
